export function getMockStudentList(
  params?: Api.SystemManage.StudentSearchParams
): Promise<Api.SystemManage.StudentList> {
  return new Promise(resolve => {
    setTimeout(() => {
      let filteredData = [...mockStudents];

      if (params) {
        if (params.studentName) {
          filteredData = filteredData.filter(s => s.studentName.includes(params.studentName!));
        }
        if (params.studentNo) {
          filteredData = filteredData.filter(s => s.studentNo.includes(params.studentNo!));
        }
        if (params.studentGender) {
          filteredData = filteredData.filter(s => s.studentGender === params.studentGender);
        }
        if (params.status !== null && params.status !== undefined) {
          filteredData = filteredData.filter(s => s.status === params.status);
        }
      }

      const current = params?.current || 1;
      const size = params?.size || 10;
      const start = (current - 1) * size;
      const end = start + size;
      const paginatedData = filteredData.slice(start, end);

      resolve({
        records: paginatedData,
        total: filteredData.length,
        current,
        size
      });
    }, 500);
  });
}

